package userModel;

public class User {
	private String username;
	private String password;
	public String getUsername()
	{
		return username;
	}
	public String getPassword()
	{
		return password;
	}
	public void setUsername(String tmp)
	{
		this.username = tmp;
	}
	public void setPassword(String tmp)
	{
		this.password = tmp;
	}
}
